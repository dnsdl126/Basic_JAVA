package problem;


public class ReversePrint {
	// 조건
	// 1. 사용자가 임의의 정수를 입력
	// 2. 사용자가 입력한 정수를 1부터 끝 까지 출력
	// 출력 예제
	// 입력>>5
		
		public void num(int num2) {
			
			
			int num1;
		
			
			for(int i = 1; i < num2 ; i++) {
				num1 = num2 - i;
			}
				
				
			
			System.out.println(num1);
			
		}
}